from hand import hand


def test_hand_nothing():
    assert hand(["K♠", "A♦"], ["J♣", "Q♥", "9♥", "2♥", "3♦"]) == ("nothing", ["A", "K", "Q", "J", "9"])


def test_hand_pair():
    assert hand(["K♠", "Q♦"], ["J♣", "Q♥", "9♥", "2♥", "3♦"]) == ("pair", ["Q", "K", "J", "9"])


def test_hand_two_pair():
    assert hand(["K♠", "J♦"], ["J♣", "K♥", "9♥", "2♥", "3♦"]) == ("two pair", ["K", "J", "9"])


def test_hand_three_of_a_kind():
    assert hand(["4♠", "9♦"], ["J♣", "Q♥", "Q♠", "2♥", "Q♦"]) == ("three-of-a-kind", ["Q", "J", "9"])


def test_hand_straight():
    assert hand(["Q♠", "2♦"], ["J♣", "10♥", "9♥", "K♥", "3♦"]) == ("straight", ["K", "Q", "J", "10", "9"])


def test_hand_flush():
    assert hand(["A♠", "K♦"], ["J♥", "5♥", "10♥", "Q♥", "3♥"]) == ("flush", ["Q", "J", "10", "5", "3"])


def test_hand_full_house():
    assert hand(["A♠", "A♦"], ["K♣", "K♥", "A♥", "Q♥", "3♦"]) == ("full house", ["A", "K"])


def test_hand_four_of_a_kind():
    assert hand(["2♠", "3♦"], ["2♣", "2♥", "3♠", "3♥", "2♦"]) == ("four-of-a-kind", ["2", "3"])


def test_hand_straight_flush():
    assert hand(["8♠", "6♠"], ["7♠", "5♠", "9♠", "J♠", "10♠"]) == ("straight-flush", ["J", "10", "9", "8", "7"])

def test_randoms_from_codewars():
    assert hand(['6♥', '4♠'], ['3♠', '10♥', 'A♣', '8♣', '2♠']) == ('nothing', ['A', '10', '8', '6', '4'])
    assert hand(['7♠', '2♠'], ['A♠', '10♠', 'A♣', 'A♦', 'K♠']) == ('flush', ['A', 'K', '10', '7', '2'])
    assert hand(['7♦', 'A♠'], ['10♠', '7♠', '2♠', '5♣', 'K♠']) == ('flush', ['A', 'K', '10', '7', '2'])
    assert hand(['4♦', '5♦'], ['8♦', '6♦', 'A♠', '7♦', '7♥']) == ('straight-flush', ['8', '7', '6', '5', '4'])
    assert hand(['Q♦', '6♠'], ['K♠', 'Q♠', '8♠', '4♠', '9♠']) == ('flush', ['K', 'Q', '9', '8', '6'])
    assert hand(['6♥', '9♥'], ['10♥', '7♥', '7♦', '8♥', '9♠']) == ('straight-flush', ['10', '9', '8', '7', '6'])
    assert hand(['4♣', '6♥'], ['9♥', '3♥', 'K♥', '6♣', '8♥']) == ('flush', ['K', '9', '8', '6', '3'])
    assert hand(['4♠', '6♠'], ['3♠', '7♥', '7♠', '7♦', '5♠']) == ('straight-flush', ['7', '6', '5', '4', '3'])
    assert hand(['5♣', '4♣'], ['6♣', '4♠', '4♦', '2♣', 'A♣']) == ('flush', ['A', '6', '5', '4', '2'])
    assert hand(['9♣', '8♣'], ['7♣', '6♣', '10♣', '8♠', 'K♠']) == ('straight-flush', ['10', '9', '8', '7', '6'])
    assert hand(['8♥', '3♠'], ['5♥', '7♠', 'Q♣', 'A♦', '10♠']) == ('nothing', ['A', 'Q', '10', '8', '7'])
    assert hand(['A♥', 'J♦'], ['5♦', '10♦', '8♦', '7♠', '9♦']) == ('flush', ['J', '10', '9', '8', '5'])
    assert hand(['3♠', '6♦'], ['7♣', '5♥', '4♣', '5♠', 'Q♥']) == ('straight', ['7', '6', '5', '4', '3'])
    assert hand(['7♠', '9♠'], ['10♠', 'J♠', '4♣', '8♠', '8♦']) == ('straight-flush', ['J', '10', '9', '8', '7'])
    assert hand(['10♠', '2♥'], ['9♥', '7♥', '3♥', '9♣', 'J♥']) == ('flush', ['J', '9', '7', '3', '2'])
    assert hand(['J♠', '9♣'], ['8♥', '4♦', '10♠', 'K♣', '5♥']) == ('nothing', ['K', 'J', '10', '9', '8'])
    assert hand(['A♥', '2♣'], ['9♣', '8♣', 'A♣', '3♠', 'J♣']) == ('flush', ['A', 'J', '9', '8', '2'])
    assert hand(['3♠', '8♣'], ['5♠', '4♠', '5♦', '7♠', '6♠']) == ('straight-flush', ['7', '6', '5', '4', '3'])
    assert hand(['5♣', 'Q♣'], ['K♣', '2♣', 'A♣', '9♠', 'Q♦']) == ('flush', ['A', 'K', 'Q', '5', '2'])
    assert hand(['7♥', '4♣'], ['6♦', '5♠', '8♠', '5♥', '10♠']) == ('straight', ['8', '7', '6', '5', '4'])
    assert hand(['Q♦', '2♥'], ['6♠', '4♦', '3♥', '5♠', '5♦']) == ('straight', ['6', '5', '4', '3', '2'])
    assert hand(['8♦', '4♥'], ['8♥', 'Q♣', 'K♥', '9♥', '6♥']) == ('flush', ['K', '9', '8', '6', '4'])
    assert hand(['10♥', '2♠'], ['5♦', 'Q♦', '7♥', 'J♦', 'A♦']) == ('nothing', ['A', 'Q', 'J', '10', '7'])
    assert hand(['9♠', '8♠'], ['10♠', 'J♦', '6♠', '10♣', '7♠']) == ('straight-flush', ['10', '9', '8', '7', '6'])
    assert hand(['K♥', '2♠'], ['9♥', '7♠', '6♥', '7♥', '5♥']) == ('flush', ['K', '9', '7', '6', '5'])
    assert hand(['9♦', 'A♣'], ['7♣', '8♣', '4♣', '10♣', 'K♣']) == ('flush', ['A', 'K', '10', '8', '7'])
    assert hand(['9♣', 'K♣'], ['5♣', '7♣', 'A♣', '7♠', '2♦']) == ('flush', ['A', 'K', '9', '7', '5'])
    assert hand(['5♣', '8♠'], ['10♦', '4♠', '3♠', '7♦', '9♦']) == ('nothing', ['10', '9', '8', '7', '5'])
    assert hand(['2♣', 'K♥'], ['Q♥', '7♥', '10♣', '3♦', 'A♥']) == ('nothing', ['A', 'K', 'Q', '10', '7'])
    assert hand(['Q♦', 'J♣'], ['9♦', '8♦', '10♦', '5♦', '8♥']) == ('flush', ['Q', '10', '9', '8', '5'])
    assert hand(['2♦', '4♣'], ['5♣', '6♥', '3♦', '5♥', '7♥']) == ('straight', ['7', '6', '5', '4', '3'])
    assert hand(['2♦', 'Q♦'], ['4♦', '3♦', 'J♥', '2♥', '7♦']) == ('flush', ['Q', '7', '4', '3', '2'])
    assert hand(['4♣', 'J♦'], ['2♠', '9♣', '6♣', 'K♣', '10♣']) == ('flush', ['K', '10', '9', '6', '4'])
    assert hand(['2♣', '9♠'], ['6♠', '10♠', '7♥', '7♠', '8♠']) == ('straight-flush', ['10', '9', '8', '7', '6'])
    assert hand(['3♥', '6♦'], ['7♦', '5♠', '4♥', '6♣', '9♦']) == ('straight', ['7', '6', '5', '4', '3'])
    assert hand(['10♦', 'J♠'], ['8♠', '9♠', '10♠', 'Q♠', 'K♠']) == ('straight-flush', ['K', 'Q', 'J', '10', '9'])
    assert hand(['8♣', '2♦'], ['K♠', '4♠', '10♣', '9♠', '5♦']) == ('nothing', ['K', '10', '9', '8', '5'])
    assert hand(['A♠', '4♣'], ['J♥', '8♥', '5♦', '10♣', '6♣']) == ('nothing', ['A', 'J', '10', '8', '6'])
    assert hand(['J♠', '8♠'], ['Q♦', '7♠', '5♠', 'A♠', 'Q♣']) == ('flush', ['A', 'J', '8', '7', '5'])
    assert hand(['Q♠', '10♥'], ['3♣', '10♠', '3♠', '7♠', 'J♠']) == ('flush', ['Q', 'J', '10', '7', '3'])
    assert hand(['7♣', '9♣'], ['K♣', '8♦', '6♠', '6♣', '3♣']) == ('flush', ['K', '9', '7', '6', '3'])
